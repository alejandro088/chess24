    public function SetupMatches()
    {
        $groups = Group::all();
        foreach($groups as $group){
            $rivals = $group->players;
            
            foreach($rivals as $rival)
                foreach ($group->players as $player)
                    if($rival != $player){ 
                        Match::create([
                            'white_player_id' => $rival->id,
                            'black_player_id' => $player->id,
                            'created_at' => NULL,
                            'updated_at' => NULL
                        ]);                        
                    }
        }
    }

